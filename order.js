document.addEventListener("DOMContentLoaded", function() {

    const params      = new URLSearchParams(window.location.search);
    const productName = params.get("product");
    const product     = products[productName];

    const productImage    = document.getElementById("product-image");
        productImage.src            = product.image;
        productImage.alt            = productName;

    const productNameEl   = document.getElementById("product-name");
        productNameEl.textContent   = productName;

    const productCategory = document.getElementById("product-category");
         productCategory.textContent = product.category;

    const notFound        = document.getElementById("product-not-found");
    const sizeSelect      = document.getElementById("size-select");
    const qtyMinus        = document.getElementById("qty-minus");
    const qtyPlus         = document.getElementById("qty-plus");
    const qtyInput        = document.getElementById("quantity");
    const unitPriceEl     = document.getElementById("unit-price");
    const totalPriceEl    = document.getElementById("total-price");
    const confirmBtn      = document.getElementById("confirm-btn");
    const successMsg      = document.getElementById("order-success");
    const errorMsg        = document.getElementById("order-error");

    if (!product) {
        notFound.style.display     = "block";
        productImage.style.display = "none";
        confirmBtn.disabled        = true;
        return;
    }

    product.sizes.forEach(function(size, index) {
        const option       = document.createElement("option");
        option.value       = index;
        option.textContent = size.name + " — $" + size.price.toFixed(2);
        sizeSelect.appendChild(option);
    });

    function updatePrice() {
        const selectedSize           = product.sizes[sizeSelect.value];
        const quantity               = parseInt(qtyInput.value);
        unitPriceEl.textContent      = "$" + selectedSize.price.toFixed(2);
        totalPriceEl.textContent     = "$" + (selectedSize.price * quantity).toFixed(2);
    }

    updatePrice();
    sizeSelect.addEventListener("change", updatePrice);

    qtyMinus.addEventListener("click", function() {
        const current = parseInt(qtyInput.value);
        if (current > 1) { qtyInput.value = current - 1; updatePrice(); }
    });

    qtyPlus.addEventListener("click", function() {
        const current = parseInt(qtyInput.value);
        if (current < 20) { qtyInput.value = current + 1; updatePrice(); }
    });

    confirmBtn.addEventListener("click", async function() {
        successMsg.style.display = "none";
        errorMsg.style.display   = "none";

        const selectedSize = product.sizes[sizeSelect.value];
        const quantity     = parseInt(qtyInput.value);

        const order = {
            drinkName  : productName,
            size       : selectedSize.name,
            quantity   : quantity,
            unitPrice  : selectedSize.price,
            totalPrice : parseFloat((selectedSize.price * quantity).toFixed(2)),
            orderDate  : new Date().toISOString()
        };

        confirmBtn.disabled    = true;
        confirmBtn.textContent = "Placing order...";

        try {
            await addDoc(collection(db, "Orders"), order);
            successMsg.style.display = "block";
            confirmBtn.textContent   = "Order Placed ✔";
        } catch (error) {
            console.error("Firebase error:", error);
            errorMsg.style.display = "block";
            confirmBtn.disabled    = false;
            confirmBtn.textContent = "Confirm Order";
        }
    });

});